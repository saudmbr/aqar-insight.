/** قائمة المناطق */
export const SAUDI_REGIONS_LIST = Object.keys(SAUDI_GEO);

/** محافظات منطقة معينة */
export function getMuhafazat(region: string): string[] {
  return Object.keys(SAUDI_GEO[region] ?? {});
}

/** مراكز محافظة معينة */
export function getMarakiz(region: string, muhafaza: string): string[] {
  return Object.keys(SAUDI_GEO[region]?.[muhafaza] ?? {});
}

/** أحياء مركز معين */
export function getAhyaa(region: string, muhafaza: string, markaz: string): string[] {
  return SAUDI_GEO[region]?.[muhafaza]?.[markaz] ?? [];
}

/** كل أحياء محافظة معينة (من جميع مراكزها) */
export function getAllAhyaaForCity(region: string, muhafaza: string): string[] {
  const muhData = SAUDI_GEO[region]?.[muhafaza];
  if (!muhData) return [];
  return [...new Set(Object.values(muhData).flat())].sort();
}

/** كل المحافظات مسطحة */
export const ALL_MUHAFAZAT: string[] = [
  ...new Set(
    Object.values(SAUDI_GEO).flatMap(region => Object.keys(region))
  ),
].sort();

/** كل المراكز مسطحة */
export const ALL_MARAKIZ: string[] = [
  ...new Set(
    Object.values(SAUDI_GEO).flatMap(region =>
      Object.values(region).flatMap(muh => Object.keys(muh))
    )
  ),
].sort();

/** للتوافق مع الكود القديم — تعيين محافظة → منطقة */
export const MUHAFAZA_TO_REGION: Record<string, string> = Object.fromEntries(
  Object.entries(SAUDI_GEO).flatMap(([region, muhs]) =>
    Object.keys(muhs).map(muh => [muh, region])
  )
);

/** للتوافق مع الكود القديم — محافظات كل منطقة بالاسم القديم */
export const SAUDI_REGIONS: Record<string, string[]> = Object.fromEntries(
  Object.entries(SAUDI_GEO).map(([region, muhs]) => [region, Object.keys(muhs)])
);

/** كل المدن/المحافظات */
export const SAUDI_CITIES: string[] = ALL_MUHAFAZAT;
